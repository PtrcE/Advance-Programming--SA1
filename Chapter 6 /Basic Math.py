import math

ceil_a = math.ceil(2.3)
print(f'Ceil of a: {ceil_a}')

floor_a = math.floor(2.3)
print(f'Floor of a: {floor_a}')

factorial_a = math.factorial(5)
print(f'Factorial of a: {factorial_a}')

value_of_23 = math.pow(2, 3)
print(f'Value of 2 to the power of 3: {value_of_23}')

square_root_a = math.sqrt(16)
print(f'Square root of a: {square_root_a}')